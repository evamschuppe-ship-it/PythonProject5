def get_3_letter_words(book_file):
    """
    Read contents of the book file and print 3 letter words starting with a B
    :param book_file: the filename of the book file
    :return: None
    """

    unique_words = []
    special_chars = ",?.!:;"
    with open(book_file) as f:
        for line in f:
            for c in special_chars:
                line = line.replace(c, "")
            words = line.split()
            for word in words:
                if word not in unique_words and len(word) ==3 and word [0] == "b":
                    unique_words.append(word)
    print(f"We have {len(unique_words)} unique words")
    print(unique_words)

